public class PerfectDay {
  public static void main(String[] args) {
    System.out.println("Here's my perfect day:");
    morning();
    afternoon();
    night();
  }//end main method


  public static void morning() {
    System.out.print("In the morning,");
    System.out.println("I would wake up late and skip breakfast");
    System.out.println("I will play games and watch videos for a hour");
  }//end morning method

  public static void afternoon() {
    System.out.println("At 2:00 I would go hang out with friends");
    System.out.print("We would go get food at a Korean BBQ place, ");
    System.out.println("then we would go play 8 ball pool");
  }//end afternoon method

  public static void night() {
    System.out.println("At 6:00 or 7:00, I would go home and shower");
    System.out.print("Afterwards, I would try to do something productive, ");
    System.out.print("such as drawing, exercising or studying");
    System.out.println("Finally, I would play around for a bit more before I sleep");
  }//end night method
}//end class (first line)

